/**
 * Auto-generate CURRENT-QUALITY-ASSESSMENT.md from GATE-REPORT.json
 *
 * Run: node generate-quality-assessment.mjs
 * Prereq: npm run gates (produces GATE-REPORT.json)
 */
import { readFileSync, writeFileSync } from 'fs';

const FEATURE_GATES = {
  'Container System':    ['G1-store', 'G2-addContainer'],
  'View Modes':          ['G3-viewMode'],
  'Undo/Redo':           ['G4-undo', 'G19-undoBehavioral'],
  'Custom Palettes':     ['G5-palette'],
  'Staircase System':    ['G6-staircase', 'G15-stairPreview'],
  'Export':              ['G7-export'],
  'Voxel Selection':     ['G8-selectedVoxel'],
  'Launch UX':           ['G9-noStartScreen'],
  'Model Homes':         ['G10-modelHome'],
  'Camera':              ['G11-camera'],
  'Ground/Atmosphere':   ['G12-noGrass'],
  'Debug Mode':          ['G13-debugMode'],
  'UI Polish':           ['G14-resetBtn'],
  'Structural Frame':    ['G16-frameStructure', 'G17-frameToggle'],
  'Theme System':        ['G18-themeSwitch'],
};

const UNGATED_FEATURES = [
  'Furniture',
  'Walkthrough',
  'Blueprint',
  'Save/Load',
  'Door System',
  'Extension System',
  'Adjacency',
];

let report;
try {
  report = JSON.parse(readFileSync('GATE-REPORT.json', 'utf8'));
} catch (e) {
  console.error('GATE-REPORT.json not found. Run `npm run gates` first.');
  process.exit(1);
}

const gateMap = new Map(report.gates.map(g => [g.gate, g]));

function rate(gateIds) {
  if (!gateIds.length) return 'UNVERIFIED';
  const allPass = gateIds.every(id => gateMap.get(id)?.status === 'PASS');
  return allPass ? 'PRODUCTION' : 'BROKEN';
}

const rows = [];
for (const [feature, gateIds] of Object.entries(FEATURE_GATES)) {
  const rating = rate(gateIds);
  const evidence = gateIds.map(id => {
    const g = gateMap.get(id);
    return g ? `${id}: ${g.status} — ${g.msg}` : `${id}: NOT RUN`;
  }).join('; ');
  rows.push({ feature, rating, evidence, gateIds });
}

const counts = { PRODUCTION: 0, BROKEN: 0, UNVERIFIED: 0 };
rows.forEach(r => counts[r.rating]++);

const now = new Date().toISOString().split('T')[0];

let md = `# Current Quality Assessment — Auto-Generated

**Date:** ${now}
**Sprint:** ${report.sprint}
**Method:** Auto-generated from GATE-REPORT.json (${report.passed}/${report.total} gates PASS)
**Generator:** \`npm run quality\` (generate-quality-assessment.mjs)

> **Do not edit this file manually.** Run \`npm run gates && npm run quality\` to regenerate.

---

## Feature Ratings

| Feature | Rating | Gates | Evidence |
|---------|--------|-------|----------|
`;

for (const r of rows) {
  md += `| ${r.feature} | **${r.rating}** | ${r.gateIds.join(', ')} | ${r.evidence} |\n`;
}

md += `
---

## Summary

| Rating | Count | Features |
|--------|-------|----------|
| PRODUCTION | ${counts.PRODUCTION} | ${rows.filter(r => r.rating === 'PRODUCTION').map(r => r.feature).join(', ') || '—'} |
| BROKEN | ${counts.BROKEN} | ${rows.filter(r => r.rating === 'BROKEN').map(r => r.feature).join(', ') || '—'} |
| UNVERIFIED | ${counts.UNVERIFIED} | ${rows.filter(r => r.rating === 'UNVERIFIED').map(r => r.feature).join(', ') || '—'} |

**Sprint ${report.sprint} — ${counts.PRODUCTION} features PRODUCTION, ${report.passed}/${report.total} gates PASS.**

---

## Ungated Features

The following features exist in V1 but have no Playwright gates. They cannot be rated PRODUCTION until gates are added.

${UNGATED_FEATURES.map(f => `- ${f}`).join('\n')}

---

## Gate Report Source

- Timestamp: ${report.timestamp}
- Total gates: ${report.total}
- Passed: ${report.passed}
- Failed: ${report.failed}
`;

writeFileSync('CURRENT-QUALITY-ASSESSMENT.md', md);
console.log(`Wrote CURRENT-QUALITY-ASSESSMENT.md (${counts.PRODUCTION} PRODUCTION, ${counts.BROKEN} BROKEN, ${counts.UNVERIFIED} UNVERIFIED)`);
